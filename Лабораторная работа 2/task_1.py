money_capital = 20000  # Подушка безопасности
salary = 5000  # Ежемесячная зарплата
spend = 6000  # Траты за первый месяц
increase = 0.05  # Ежемесячный рост цен

months = 0

# Цикл для подсчета месяцев
while money_capital >= 0:
    # Проверяем бюджет на текущий месяц
    if money_capital + salary < spend:
        break  # Если денег не хватает на расходы, прерываем цикл

    # Увеличиваем счетчик месяцев
    months += 1

    # Обновляем подушку безопасности после вычета расходов
    money_capital += salary - spend

    # Увеличиваем расходы на следующий месяц
    spend *= (1 + increase)

print("Количество месяцев, которое можно протянуть без долгов:", months)
